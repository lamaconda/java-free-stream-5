package by.jrr.maistrenko;

public class Washing {

    public void run() {
        goToBathroom();
        goToSink();
        turnOnWater();
        soapHands();
        washHands();
        takeToothpasteAndOpenTube();
        takeToothbrush();
        applyPasteOnBrush();
        closeAndPutTube();
        brushTeeth();
        washBrush();
        washFace();
        rinseMouth();
        turnOffWater();
        wipeFaceAndHands();
    }

    public void goToBathroom() {
        System.out.println("Entered the bathroom");
    }

    public void goToSink() {
        System.out.println("Went to the washstand");
    }

    public void turnOnWater() {
        System.out.println("Turned on the water");
    }

    public void soapHands() {
        System.out.println("Soaped hands");
    }

    public void washHands() {
        System.out.println("Washed hands");
    }

    public void takeToothpasteAndOpenTube() {
        System.out.println("Took the toothpaste and open the tube");
    }

    public void takeToothbrush() {
        System.out.println("Took toothbrush");
    }

    public void applyPasteOnBrush() {
        System.out.println("Put paste on the brush");
    }

    public void closeAndPutTube() {
        System.out.println("Closed and put the tube of paste");
    }

    public void brushTeeth() {
        System.out.println("Brushed teeth");
    }

    public void washBrush() {
        System.out.println("Washed the brush");
    }

    public void turnOffWater() {
        System.out.println("Turned off the water");
    }

    public void washFace() {
        System.out.println("Washed face");
    }

    public void rinseMouth() {
        System.out.println("Rinsed mouth");
    }

    public void wipeFaceAndHands() {
        System.out.println("Wiped hands");
    }
}
